    public  void validation(){
       
      String id =port_id.getText();
 
        if(id.matches("^[0-9]*$"))
        {
            port_id.setBackground(Color.green);
           
        } 
        
        else
        {
           
       port_id.setBackground(Color.red);
             JOptionPane.showMessageDialog(this, "ID: only numbers are allowed");
        }
        
    }
    private void p_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_p_deleteActionPerformed
        // TODO add your handling code here:
        validation();
         DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
          int selectedIndex = jTable2.getSelectedRow();
            try {   
                
            int id = Integer.parseInt(model.getValueAt(selectedIndex, 0).toString());
            int dialogResult = JOptionPane.showConfirmDialog (null, "Do you want to Delete the record","Warning",JOptionPane.YES_NO_OPTION);
            if(dialogResult == JOptionPane.YES_OPTION){

           Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/cargo","root","");  
            insert = con1.prepareStatement("delete from PORT where PORT_ID = ?");
            insert.setInt(1,id);
            insert.executeUpdate();
            JOptionPane.showMessageDialog(this, "Record Delete");
             port_id.setText("");
            port_name.setText("");
            con.setText("");
            region.removeAllItems();
            state.removeAllItems();
            table_update();
           
           }

        } catch (ClassNotFoundException ex) {
        
        } catch (SQLException ex) {
            
        }
    }//GEN-LAST:event_p_deleteActionPerformed

