       private void table_update() {
        int CC;
        try {
            
           Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/cargo","root","");
            insert = con1.prepareStatement("SELECT * FROM PORT");
            ResultSet Rs = insert.executeQuery();
            
            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) jTable2.getModel();
            DFT.setRowCount(0);

            while (Rs.next()) {
                Vector v2 = new Vector();
                
                for (int ii = 1; ii <= CC; ii++) {
                    
                    v2.add(Rs.getString("PORT_ID"));
                    v2.add(Rs.getString("PORT_NAME"));
                    v2.add(Rs.getString("COUNTRY"));
                    v2.add(Rs.getString("REGION"));
                    v2.add(Rs.getString("DISTRICT"));
      
                }
                DFT.addRow(v2);
            }
        } catch (Exception e) {
        }
    }
       
